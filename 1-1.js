// lt Easy 1 Two Sum 
// Get indices of the two numbers such that they add up to a specific target.

// conditons
let nums = [2,7,11,15]; 
let target = 9; 

// my answer
const twoSum = (nums, target) => {
    for (let i = 0; i < nums.length; i++) {
        for (let j = i + 1; j < nums.length; j++) {
            if (nums[i] + nums[j] === target) {
                return [i, j];
            }
        }
     }

    return [];  
};


